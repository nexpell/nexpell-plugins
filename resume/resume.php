        <div class="resume-item mb-4">
          <h4>2025 – Neuentwicklung von webSPELL-RM 3.0</h4>
          <p>Im Jahr 2025 wurde webSPELL-RM 3.0 komplett neu entwickelt mit einem modernen, responsiven Adminbereich.</p>
          <ul>
            <li>Integration und Umstieg auf Bootstrap 5.3 als Design- und Komponentenbasis</li>
            <li>Einführung eines modularen Adminsystems mit separaten Bereichen für Pricing, About, Gallery, Resume u.v.m.</li>
            <li>Verbesserte Drag-&-Drop-Sortierung mit AJAX, inklusive Seitenvorschau für Galerien und Inhalte</li>
            <li>Neuer Theme- und Template-Wechsler mit Live-Vorschau und komfortabler Speicherung</li>
            <li>Vollständig neu strukturierte Plugin-Architektur mit eigenständigen Admin-Panels und Datenbanksteuerung</li>
            <li>Systematischer Ausbau von Hilfetexten, Tooltips und modaler Vorschau zur Verbesserung der Benutzerfreundlichkeit (UX)</li>
            <li>Zahlreiche Kernfunktionen aus Version 2.1.6 wurden überarbeitet, optimiert oder komplett neu geschrieben</li>
            <li><strong>Deutliche Verbesserungen bei der Sicherheit:</strong>
              <ul>
                <li>Einführung von CSRF-Schutz in Formularen</li>
                <li>Prepared Statements für alle Datenbankzugriffe zur Vermeidung von SQL-Injections</li>
                <li>Verbesserte Benutzer- und Rechteverwaltung</li>
                <li>Sicherere Passwort-Hashing-Methoden</li>
              </ul>
            </li>
            <li>Verbesserte Performance durch moderne PHP-Standards und optimierten Code</li>
            <li>Integration neuer Features wie SEO-optimierte URL-Strukturen und bessere Mehrsprachigkeit</li>
            <li>Vereinfachte Erweiterbarkeit und Wartbarkeit durch konsequente Trennung von Logik und Darstellung</li>
          </ul>
