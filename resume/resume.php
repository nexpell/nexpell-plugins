        <div class="resume-item mb-4">
  <h4>2018 – Gründung von Webspell-RM</h4>
  <p>
    Am 12. September 2018 wurde Webspell-RM ins Leben gerufen, nachdem das Team von
    <a href="https://www.designperformance.de" target="_blank">Design Performance</a> das Projekt übernommen hatte.
    Zuvor wurde das Projekt <strong>webSPELL-NOR</strong> infolge der neuen Datenschutzgrundverordnung (DSGVO) eingestellt. 
    Auch nach Anpassungen an die Datenschutzvorgaben wurde am 31.10.2018 intern das Ende von NOR beschlossen, und die Webseite wurde abgeschaltet.
  </p>
  <p>
    Viele Ideen, die ursprünglich für NOR vorgesehen waren, konnten nicht umgesetzt werden. Mit Zustimmung der damaligen NOR-Administratoren wurde daher 
    <strong>webSPELL | RM</strong> gegründet – mit dem Ziel, diese Ideen neu zu verwirklichen und das beliebte CMS technisch wie gestalterisch auf ein neues Level zu heben.
  </p>
  <p>
    Alle bestehenden Plugins wurden vollständig überarbeitet, um mit der neuen RM-Version kompatibel zu sein. 
    <a href="https://www.Webspell-RM.de" target="_blank">Webspell-RM.de</a> ist somit das letzte aktive Webspell-Projekt.
  </p>
</div>

        <div class="resume-item mb-4">
          <h4>2019–2020 – Einführung neuer Features</h4>
          <ul>
            <li>Integration des Bootstrap 5 Frameworks für ein modernes Design</li>
            <li>Einführung des CKEditor 4 für eine verbesserte Textbearbeitung</li>
            <li>Plugin- und Template-Installer zur einfachen Erweiterung</li>
            <li>Mehrsprachigkeit: Deutsch, Englisch, Italienisch</li>
            <li>Updater für einfache Systemaktualisierungen</li>
            <li>Bis zu 84 Plugins und 13 Templates verfügbar</li>
          </ul>
        </div>

        <div class="resume-item mb-4">
